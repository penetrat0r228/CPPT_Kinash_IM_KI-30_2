package КІ302.Кінаш.Lab3;

/**
 * Інтерфейс Portable, який визначає основні операції для портативних пристроїв з акумулятором.
 */
public interface Portable {

    /**
     * Метод для зарядки акумулятора пристрою.
     */
    void chargeBattery();

    /**
     * Метод для отримання поточного рівня заряду акумулятора.
     *
     * @return поточний рівень заряду акумулятора в процентах.
     */
    int getBatteryLevel();
}